#ask for two values from a user
val1 = int(input("Please enter a number: "))
val2 = int(input("Please enter another number: "))

#add, multiply, divide and subtract with two values val1 and val2
valueSum = val1 + val2
valueMul = val1 * val2
valueDiv = val1 / val2
valueSub = val1 - val2

#print the results
print("The sum is:", valueSum)
print("The multiplied value is:", valueMul)
print("The divided value is:", valueDiv)
print("The subtracted value is:", valueSub)

